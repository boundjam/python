import turtle as t
def rectangle(horizontal,vertical,color):
    t.pendown()
    t.color(color)
    t.speed('fast')
    t.pensize(1)
    t.begin_fill()
    for counter in range(0,2):
        t.forward(horizontal)
        t.right(90)
        t.forward(vertical)
        t.right(90)
    t.end_fill()
    t.penup()
