def cheese_and_crackers(cheesesse_count, boxes_of_crackers):
    print(f"You have {cheesesse_count} cheeses!")
    print(f"You have {boxes_of_crackers} boxes_of_crackers")
    print("Man That is enough for a party!")
    print("get a blanket.\n")
print("we can just give the fuction numbers directly!:")
cheese_and_crackers(20, 30)
print("OR, we use varibles to our script:")
amount_of_cheese = 10
amount_of_crackers= 50
cheese_and_crackers(amount_of_cheese, amount_of_crackers)
print("we can do the math inside too:")
cheese_and_crackers(10 + 20, 5 + 6)
print("And we can combine the two, varibles and math:")
cheese_and_crackers(amount_of_cheese + 100, amount_of_crackers + 1000)
